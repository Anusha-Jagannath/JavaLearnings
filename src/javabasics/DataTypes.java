package javabasics;

public class DataTypes {

    public static void main(String args[]) {
        System.out.println("learning java data types");
        byte x = 2;
        System.out.println(x);
        int age = 10;
        System.out.println(age);
        float rateOfInterest = 2.3f;
        System.out.println(rateOfInterest);
        double rateOfNewValue = 2.33;
        System.out.println(rateOfNewValue);
        boolean isThisSeries = false;
        System.out.println(isThisSeries);
        char myCharacter = 'a';
        System.out.println(myCharacter);

    }
}
