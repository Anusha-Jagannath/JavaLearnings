package july5;

public class Pattern1 {

    public static void main(String args[]) {
        System.out.println("Learning pattern1");
        int n = 4;

        for (int i = 1; i <= n; i++) {

            for (int j = 1; j <= n; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
