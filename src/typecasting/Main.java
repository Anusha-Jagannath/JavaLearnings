package typecasting;

public class Main {

    public static void main(String args[]) {
        System.out.println("Learning type casting");

        byte x = 4;
        int a = x;
        System.out.println(a);

        int i = 4;
        double d = i;
        System.out.println(d);

        double myDouble = 3.84;
        int myInt = (int) myDouble;

        System.out.println(myInt);

    }
}
